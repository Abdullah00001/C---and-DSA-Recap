#include <bits/stdc++.h>
using namespace std;

int main()
{
    /* =============================
    using cin cpp take input
    and using cout cpp give or print output
    '>>' use for input and '<<' use for output
    =============================== */
    int a, b;
    cin >> a >> b;
    cout << a << " " << b << endl;
    return 0;
};